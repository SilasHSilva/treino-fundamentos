#Caracteres de escape
'''
    \n - quebra de linha
    \t - tabulação
    \b - espaço em branco
    \b - recuo do carro
    \\ - mostra barra \
    \' - mostra a aspa

'''

print ("d'agua",45)
print ('d\'agua ' + str(45))
print (12,13,14, sep=";")
print (12,13,14, sep="-", end='\n\n')
print ("nome" , "Adriano", sep=":")
